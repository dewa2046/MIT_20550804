    private void btnGetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGetActionPerformed
        // TODO add your handling code here:
        try {
            String atm = (cmbATM.getSelectedItem()).toString();
            String selectedATM = atm.split("-")[0].trim();
            String location = atm.split("-")[1].trim();
            sqlop sql = new sqlop();
            String details = sql.getATMDetails(selectedATM);
            String div = details.split("~")[0].trim();
            int protocol = Integer.parseInt(details.split("~")[1].trim());
            Other other = new Other();

            other.setAtm(selectedATM);
            other.setLocation(location);
            other.setDiv(div);
            other.setProtocol(protocol);

            if (div.equals("ATM") && protocol == 1) {
                new ATM().setVisible(true);
//                dispose();
            }
            if (div.equals("CRM") && protocol == 1) {
                new DDCCRM().setVisible(true);
//                dispose();
            }
            if (div.equals("CRM") && protocol == 2) {
                new NDCCRM().setVisible(true);
//                dispose();
            }
            if (div.equals("CDM") && protocol == 1) {
                JOptionPane.showMessageDialog(null, "Not Developed", "Error", JOptionPane.ERROR_MESSAGE);
//                dispose();
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.toString(), "Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnGetActionPerformed

