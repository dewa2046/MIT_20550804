    public void loadMachines() {

        ArrayList<String> machineDetails = new ArrayList<String>();
        sqlop sql = new sqlop();
        machineDetails = sql.getMachineList();

        if (machineDetails.size() != 0) {

            for (int i = 0; i < machineDetails.size(); i++) {
                String record = machineDetails.get(i);
                String[] recordArr = record.split("~");
                cmbATM.addItem(recordArr[0] + "- " + recordArr[1]);
            }
        } else {
            JOptionPane.showMessageDialog(null, "No machine added to Load", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

